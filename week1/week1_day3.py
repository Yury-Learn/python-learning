"""
Week1 Day3
Тема:
1. Условия (if / else)
2. Сравнение строк
3. Разница между = и ==

Сценарий:
Мы просим пользователя ввести пароль, сравниваем его с "секретным" значением.
Если совпало — доступ разрешён, если нет — отказ.
"""

# correct_password:
#   строка (str), которую мы считаем "правильным" паролем.
#   В реальном мире пароль так в коде не хранят (это небезопасно),
#   но для учебного примера это норм.
correct_password = "qwerty"

# user_input:
#   переменная, куда мы положим то, что введёт человек.
#   input("Введи пароль: ") печатает приглашение и ждёт ввода, как мы уже делали.
user_input = input("Введи пароль: ")

# ВАЖНО: оператор == это оператор СРАВНЕНИЯ.
# Он отвечает на вопрос "равно ли левое значение правому?"
# Возвращает True или False.
#
# То есть user_input == correct_password
# читается как:
#   "строка, которую ввёл пользователь, совпадает с правильным паролем?"
if user_input == correct_password:
    print("Доступ разрешён")
else:
    print("Доступ запрещён")

# Пояснение про = и ==:
#
# =  (один знак равенства) — это ПРИСВОЕНИЕ.
# Мы создаём переменную или меняем её значение.
# Пример: x = 10  (в переменную x положили число 10)
#
# == (два знака равенства) — это СРАВНЕНИЕ.
# Мы спрашиваем "равно ли x значению 10?"
# Пример: x == 10  (это не меняет x, это просто проверка: да или нет)
#
# Для наглядности:
x = 10        # присвоили значение 10 в переменную x
check = (x == 10)  # сравнили x с 10 -> результат True
print("x =", x)
print("Результат сравнения x == 10:", check, "| тип:", type(check))
